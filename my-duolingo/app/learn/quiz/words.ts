export const getWordsByChapter = (unitId: number, chapterId: number): Word[] => {
  const wordsPerChapter = 10;
  const chaptersPerUnit = 10;

  // 전체 순번 계산 (0부터 시작하는 인덱스 기준)
  // 예: Unit 1, Ch 1 -> (0 * 20) + 0 = 0번째 챕터
  // 예: Unit 2, Ch 1 -> (1 * 10) + 0 = 20번째 챕터
  const globalChapterIndex = (unitId - 1) * chaptersPerUnit + (chapterId - 1);
  
  const startId = globalChapterIndex * wordsPerChapter + 1;
  const endId = startId + wordsPerChapter - 1;

  return words.filter((word) => word.id >= startId && word.id <= endId);
};